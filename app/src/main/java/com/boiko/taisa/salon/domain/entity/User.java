package com.boiko.taisa.salon.domain.entity;

public class User {
    private String id;
    private String name;
    private String role;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
